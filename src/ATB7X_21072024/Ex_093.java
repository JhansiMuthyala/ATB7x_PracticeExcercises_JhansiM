package ATB7X_21072024;

public class Ex_093 {
    //Lab_113
    public static void main(String[] args) {
// numberal - float, int, short, byte,long, char, double
        for(float f=0.0f;f<10.67;f++){
            System.out.println("Hi,Float -> "+ f);
        }

        for(byte b=0;b<10.67;b++){
            System.out.println("Hi,Byte -> "+b);
        }
        for (long l = 1; l < 10; l++) {
            System.out.println("Hi,Long -> "+l);
        }

        for (double d = 1.10; d < 12.88; d++) {
            System.out.println("Hi,Double -> "+d);

        }
        for (char c = 'a'; c <= 'z';c++){
            System.out.println("Hi,Char -> "+c);
        }
    }
}
