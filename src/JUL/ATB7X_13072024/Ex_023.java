package JUL.ATB7X_13072024;

public class Ex_023 {
    public static void main(String[] args) {

// lab_040
        // Compound Assignment Operator
        int a = 10;
        // +=, -=, /=, *=, %=
        // += ->
        a += 10; // a = a+10 = (10+10) = 20
        System.out.println(a);
        a -= 10; // a=a-10 (20-10 = 10)
        System.out.println(a);

        a /= 10; // a=a/10 (10/10 = 1)
        System.out.println(a);

        a *= 10; // a=a*10  (1 * 10 = 10)
        System.out.println(a);


    }
}
